#include "ft_printf.h"

void	ft_putunbr(unsigned int nb, int *len)
{
	if (nb > 9)
	{
		ft_putnbr(nb / 10, len);
		ft_putnbr(nb % 10, len);
	}
	else
		*len += ft_putchar(nb + '0');
}
